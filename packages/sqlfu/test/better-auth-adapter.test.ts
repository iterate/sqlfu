test('sqlfuBetterAuthAdapter preserves runtime methods and wraps createSchema', async () => {
  await using fixture = await createBetterAuthFixture({
    files: {
      'definitions.sql': '',
    },
  });

  const create = async () => ({id: 'created-user'});
  const baseAdapter = createBaseAdapter({create});
  const adapter = sqlfuBetterAuthAdapterUnderTest({
    sqlfu: {definitions: './definitions.sql', queries: './sql'},
    projectRoot: fixture.root,
    adapter: () => baseAdapter,
  })({});

  expect(adapter).toMatchObject({
    id: 'sqlfu',
    create,
    options: {
      adapterConfig: {
        adapterId: 'sqlfu',
        adapterName: 'sqlfu Better Auth adapter',
      },
    },
  });
  const transactionAdapter = await adapter.transaction(async (tx) => tx);

  const schema = await adapter.createSchema?.({}, 'definitions.sql');

  expect(schema).toMatchObject({
    path: 'definitions.sql',
    overwrite: true,
  });
  expect(schema?.code).toContain('-- #region sqlfu:better-auth');
  expect(schema?.code).toContain('create table "user"');
  expect(schema?.code).toContain('"email" text not null unique');
  expect(schema?.code).toContain('-- #endregion sqlfu:better-auth');
  expect(transactionAdapter).toMatchObject({
    id: 'sqlfu',
    options: {
      adapterConfig: {
        adapterId: 'sqlfu',
        adapterName: 'sqlfu Better Auth adapter',
      },
    },
  });
});

test('createSchema preserves application SQL outside the managed Better Auth section', async () => {
  await using fixture = await createBetterAuthFixture({
    files: {
      'definitions.sql': dedent`
        create table "posts" (
          "id" integer primary key not null,
          "slug" text not null unique
        );

        -- #region sqlfu:better-auth
        create table "obsolete_auth_table" ("id" text primary key not null);
        -- #endregion sqlfu:better-auth

        create table "comments" (
          "id" integer primary key not null,
          "postId" integer not null references "posts" ("id") on delete cascade
        );
      `,
    },
  });

  const schema = await createAdapterForFixture(fixture).createSchema?.({}, 'definitions.sql');

  expect(schema?.code).toMatch(/^create table "posts"/);
  expect(schema?.code).toContain('create table "comments"');
  expect(schema?.code).toContain('create table "user"');
  expect(schema?.code).not.toContain('obsolete_auth_table');
  expect(schema?.code).toMatch(
    /-- #region sqlfu:better-auth\n-- generated by Better Auth through sqlfuBetterAuthAdapter; edit Better Auth config instead\ncreate table "user"/,
  );
});

