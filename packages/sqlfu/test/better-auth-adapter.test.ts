test('createSchema rejects malformed Better Auth fences', async () => {
  const cases = [
    {
      name: 'dangling begin',
      definitionsSql: dedent`
        create table "posts" ("id" integer primary key not null);

        -- #region sqlfu:better-auth
        create table "user" ("id" text primary key not null);
      `,
      message: /exactly one sqlfu Better Auth managed section/,
    },
    {
      name: 'multiple sections',
      definitionsSql: dedent`
        -- #region sqlfu:better-auth
        create table "user" ("id" text primary key not null);
        -- #endregion sqlfu:better-auth
        -- #region sqlfu:better-auth
        create table "session" ("id" text primary key not null);
        -- #endregion sqlfu:better-auth
      `,
      message: /exactly one sqlfu Better Auth managed section/,
    },
    {
      name: 'end before begin',
      definitionsSql: dedent`
        -- #endregion sqlfu:better-auth
        create table "posts" ("id" integer primary key not null);
        -- #region sqlfu:better-auth
      `,
      message: /malformed sqlfu Better Auth managed section/,
    },
  ];

  for (const testCase of cases) {
    await using fixture = await createBetterAuthFixture({
      files: {
        'definitions.sql': testCase.definitionsSql,
      },
    });

    await expect(createAdapterForFixture(fixture).createSchema?.({}, 'definitions.sql'), testCase.name).rejects.toThrow(
      testCase.message,
    );
  }
});

